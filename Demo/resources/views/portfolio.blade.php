<h3><b>BTS SIO </b>(Services aux organisations)</h3>
​
<p>
  Le BTS Services informatiques aux organisations (SIO) est le résultat
  d une rénovation en profondeur du BTS Informatique de gestion.
  Il remplace ce dernier depuis la rentrée 2011.</br>

  Le brevet de technicien supérieur Services informatiques aux organisations est un cursus d'études se déroulant sur deux ans dans des lycées français, des écoles privées ou dans des centres de formation professionnelle. Il peut être préparé en formation continue, en alternance, par correspondance avec le Cned ou en formation initiale. Pour ma part, je le prépare en formation continue.</br>

  Dès le deuxième semestre de la première année, une option doit être choisie par l'étudiant entre :</br>

  Solutions Logicielles et Applications Métier (SLAM) ;
  Solutions d’Infrastructure, Systèmes et Réseaux (SISR).</br>

  J'ai choisi l'option SLAM.
</br>
​</p>

<h3><b>Spécialité SLAM </b>(Solutions Logicielles et Applications Métier)</h3>
</br>
<p>
  Le titulaire du diplôme participe à la production et à la fourniture de services en développant, en adaptant ou en maintenant des solutions applicatives. Il intervient plus particulièrement dans :

  - la définition des spécifications techniques à partir de l’expression des besoins des utilisateurs et des contraintes de l’organisation préalablement recensées
  - la réalisation ou l’adaptation puis la validation de solutions applicatives à l’aide des environnements de développement retenus
  - la gestion du patrimoine applicatif
  - la rédaction de la documentation d’une solution applicative et la formation des utilisateurs
  - la recherche de réponses adaptées à des problèmes liés à la fourniture des services informatiques
  - l’accompagnement et l’assistance des utilisateurs
  - le maintien de la qualité des services informatiques
</p>
